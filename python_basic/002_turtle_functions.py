import turtle
import time

jai_turtle = turtle.Turtle()
def square():    
    jai_turtle.forward(100)
    jai_turtle.right(90)
    jai_turtle.forward(100)
    jai_turtle.right(90)
    jai_turtle.forward(100)
    jai_turtle.right(90)
    jai_turtle.forward(100)
    
square()

jai_turtle.forward(100)

square()
time.sleep(3)
