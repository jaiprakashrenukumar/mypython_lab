name = "Jaiprakash"
print(name)
print("\n")

#print first letter  of the name
print("print first letter  of the name ")
print(name[0])
print("\n")

# print last letter from the name
print("print last letter from the name ")
print(name[9])
print("\n")

# remove first three character from the name
print("remove first three character from the name") 
print(name[3:])
print("\n")

# print only first 3 characters
print("print only first 3 characters")
print(name[:3])
print("\n")
